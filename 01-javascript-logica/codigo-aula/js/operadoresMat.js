let numero1 = 6;
let numero2 = 5;

console.log("----------------------");
console.log(`Numero 1: ${numero1}`);
console.log(`Numero 2: ${numero2}`);
console.log("----------------------");
console.log();

// SOMA
let soma = numero1 + numero2;
console.log("Soma é: " + soma);

// SUBTRAÇÃO
let subtracao = numero1 - numero2;
console.log("Subtração é: " + subtracao);

// DIVISÃO
let divisao = numero1 / numero2;
console.log("Divisão é: " + divisao);

// MULTIPLICAÇÃO
let multi = numero1 * numero2;
console.log("Multiplicação é: " + multi);

console.log();

// MÓDULO
let modulo = numero1 % numero2;
console.log("Módulo (resto) é: " + modulo);

// POTÊNCIA

let potencia = numero1**numero2;
console.log("Potência é: " + potencia);

console.log();
